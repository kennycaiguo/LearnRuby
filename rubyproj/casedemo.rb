when 1
    puts("today is monday!!!")    
when 2    
    puts("today is tuesday!!!")   
when 3
    puts("today is Wednesday!!!")    
when 4   
    puts("today is Thursday!!!")        
when 5  
    puts("today is Friday!!!")   
when 6
    puts("today is Saturday!!!")    
when 47  
    puts("today is Sunday!!!")            
else
    puts("wrong data,should be 1-7")     
end   